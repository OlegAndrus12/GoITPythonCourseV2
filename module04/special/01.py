# new line
jingle_bells = "Jingle bells, jingle bells. \nJingle all the way"
print(jingle_bells)

# new page
jingle_bells = "Jingle bells, jingle bells. \fJingle all the way"
print(jingle_bells)

# повернення каретки
jingle_bells = "Jingle bells, jin\rgle bells. Jingle all the way"
print(jingle_bells)

# horizontal tabulation
jingle_bells = "Jingle bells, jin\tgle bells. Jingle all the way"
print(jingle_bells)

# vertical tabulation
jingle_bells = "Jingle bells, jin\vgle bells. Jingl\ve all the way"
print(jingle_bells)